#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
		char *s;

	if (n == -2147483648)
	{
		s = "-2147483648";
		ft_atoi(s);
		while (*s)
		{
			write(1, &s, 1);
			s++;
		}
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
}
