#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	k;
	char	*str;

	i = ft_strlen(s1);
	j = ft_strlen(s2);
	str = (char *)malloc(i + j + 1);
	if (!str)
		return (NULL);
	k = 0;
	while (*s1)
		str[k++] = *s1++;
	while (*s2)
		str[k++] = *s2++;
	str[k] = '\0';
	return (str);
}
