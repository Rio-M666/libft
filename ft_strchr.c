#include <string.h>

char	*ft_strchr(const char *str, int c)
{
	unsigned char a;
	size_t i;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
		{
			return (i);
		}
		i++;
	}
	return (NULL);
}